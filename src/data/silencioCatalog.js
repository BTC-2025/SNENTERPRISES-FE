// Helper function to format price in INR
export const formatPrice = (price) => {
    return new Intl.NumberFormat('en-IN', {
        style: 'currency',
        currency: 'INR',
        minimumFractionDigits: 2,
        maximumFractionDigits: 2
    }).format(price);
};

// Helper function to get all Silencio products as flat array
export const getAllSilencioProducts = () => {
    const allProducts = [];
    silencioCatalog.sections.forEach((section, sectionIndex) => {
        section.items.forEach((item, itemIndex) => {
            allProducts.push({
                id: `silencio-${sectionIndex}-${itemIndex}`,
                ...item,
                sectionName: section.name,
                hsnCode: silencioCatalog.hsn_code
            });
        });
    });
    return allProducts;
};

// Get unique sizes for filter
export const getSilencioSizes = () => {
    const allProducts = getAllSilencioProducts();
    return [...new Set(allProducts.map(p => p.size_mm))].sort((a, b) => parseInt(a) - parseInt(b));
};

// Get unique lengths for filter
export const getSilencioLengths = () => {
    const allProducts = getAllSilencioProducts();
    return [...new Set(allProducts.map(p => p.length))];
};
