// Helper function to format price in INR
export const formatPrice = (price) => {
    return new Intl.NumberFormat('en-IN', {
        style: 'currency',
        currency: 'INR',
        minimumFractionDigits: 2,
        maximumFractionDigits: 2
    }).format(price);
};

// Helper function to get all fittings as flat array
export const getAllFittings = () => {
    const allFittings = [];
    fittingsCatalog.sections.forEach((section, sectionIndex) => {
        section.items.forEach((item, itemIndex) => {
            allFittings.push({
                id: `fitting-${sectionIndex}-${itemIndex}`,
                ...item,
                sectionName: section.name,
                hsnCode: fittingsCatalog.hsn_code
            });
        });
    });
    return allFittings;
};

// Get fitting types for filter
export const getFittingTypes = () => {
    return fittingsCatalog.sections.map(section => section.name);
};
